def square_sum(numbers):
    counter = 0
    for i in numbers:
         
        #print (i**2)
        counter = counter + (i **2)
    return counter
    
        
    
    
    
square_sum([1,2]) # 'squareSum did not return a value')
square_sum([1,2]) # 5)
square_sum([0, 3, 4, 5]) # 50)